#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t	ctr;
	char	*str;

	ctr = 0;
	str = (char *)s;
	while (ctr < n)
	{
		str[ctr] = 0;
		ctr++;
	}
}
